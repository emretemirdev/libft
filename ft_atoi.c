#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	neg;
	int	res;

	i = 0;
	neg = 1;
	res = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		res = (str[i] - '0') + (res * 10);
		i++;
	}
	return (res * neg);
}
#include <stdio.h>
int main()
{
	char str[] = "  -2147483649";
	printf("%d", ft_atoi(str));
}