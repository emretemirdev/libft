#include "libft.h"
#include <stdio.h>
int	ft_tolower(int b)
{
	if (b >= 'A' && b <= 'Z')
	{
		b += 32;
	}
	return (b);
}

int main()
{
	int b = 'A';
	printf("%c\n", ft_tolower(b));
}