#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;

	str = (char *)s;
	while (*str != (char)c)
		if (*str++ == '\0')
			return (NULL);
	return (str);
}

#include <stdio.h>
int main()
{
	char str[] = "HelloWorldHll";
	int c = 'l';
	printf("%s",ft_strchr(str,c));
}
